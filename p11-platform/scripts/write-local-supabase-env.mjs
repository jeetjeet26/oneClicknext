import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const rootDir = resolve(__filename, "..", "..");
const envLocalPath = resolve(rootDir, ".env.local");
const blockStart = "# BEGIN LOCAL SUPABASE";
const blockEnd = "# END LOCAL SUPABASE";

function getSupabaseStatusEnv() {
  const output = execFileSync("npx", ["supabase", "status", "-o", "env"], {
    cwd: rootDir,
    encoding: "utf8",
  });

  const env = {};
  for (const line of output.split("\n")) {
    const match = line.match(/^([A-Z0-9_]+)="(.*)"$/);
    if (!match) continue;
    const [, key, value] = match;
    env[key] = value.replace(/\\"/g, '"');
  }

  return env;
}

function requireValue(env, key) {
  const value = env[key];
  if (!value) {
    throw new Error(`Missing ${key} in 'supabase status -o env' output.`);
  }
  return value;
}

function upsertManagedBlock(existingContent, managedBlock) {
  if (existingContent.includes(blockStart) && existingContent.includes(blockEnd)) {
    const pattern = new RegExp(`${blockStart}[\\s\\S]*?${blockEnd}`, "m");
    return existingContent.replace(pattern, managedBlock);
  }

  const trimmed = existingContent.trimEnd();
  if (!trimmed) return `${managedBlock}\n`;
  return `${trimmed}\n\n${managedBlock}\n`;
}

const env = getSupabaseStatusEnv();
const managedEntries = {
  NEXT_PUBLIC_SUPABASE_URL: requireValue(env, "API_URL"),
  // The app's current SSR/browser auth flow is validated against the anon JWT key.
  // Keep the local overlay compatible until the app fully switches to publishable keys everywhere.
  NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY: requireValue(env, "ANON_KEY"),
  NEXT_PUBLIC_SUPABASE_ANON_KEY: requireValue(env, "ANON_KEY"),
  SUPABASE_URL: requireValue(env, "API_URL"),
  SUPABASE_SERVICE_ROLE_KEY: requireValue(env, "SERVICE_ROLE_KEY"),
  NEXT_PUBLIC_SITE_URL: "http://localhost:3000",
  DATA_ENGINE_URL: "http://localhost:8000",
};

const blockLines = [
  blockStart,
  "# Generated by `npm run supabase:env:local`.",
  "# Safe local overrides that keep web/data-engine pointed at local Supabase.",
  ...Object.entries(managedEntries).map(([key, value]) => `${key}=${value}`),
  blockEnd,
];

const existingContent = existsSync(envLocalPath) ? readFileSync(envLocalPath, "utf8") : "";
const nextContent = upsertManagedBlock(existingContent, blockLines.join("\n"));

writeFileSync(envLocalPath, nextContent);

console.log(`Updated ${envLocalPath} with local Supabase settings.`);
